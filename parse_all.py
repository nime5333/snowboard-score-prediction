import re

# Helper to parse spin degrees from trick code number
def parse_spin(trick):
    # Find numbers in trick that represent spin (5,7,9,10,12,14,16,18,19,21)
    spin_map = {'3':300,'5':500,'7':700,'9':900,'10':1080,'12':1260,'14':1440,'16':1600,'18':1800,'19':1980,'21':2160,'1':180}
    # Find standalone numbers
    nums = re.findall(r'\b(\d+)\b', trick)
    for n in nums:
        if n in spin_map:
            return spin_map[n]
    return None

def parse_trick(trick):
    if not trick or trick in ('DNS','DNI','DNF','NPS',''):
        return None
    t = trick.strip()
    # Cab is a switch frontside rotation (fakie takeoff) so counts as switch
    clean_check = t.lstrip('x-')
    switch = 'Y' if t.startswith('x-') or clean_check.startswith('Cab') or clean_check.startswith('cab') else 'N'
    double_cork = 'Y' if any(x in t for x in ['-D-','-DC-','D-','DC-']) and 'Ddr' not in t and 'Tdr' not in t else 'N'
    triple_cork = 'Y' if any(x in t for x in ['-T-','-TC-','T-','TC-']) else 'N'
    rodeo = 'Y' if 'Rd' in t or 'Rd-' in t else 'N'
    
    # Direction
    clean = t.lstrip('x-')
    if clean.startswith('Cab') or clean.startswith('cab'):
        direction = 'Cab'
    elif clean.startswith('f-') or clean.startswith('f ') or clean[0:2] in ('f-','fF'):
        direction = 'f'
    elif clean.startswith('b-') or clean.startswith('b ') or clean[0:2] == 'b-':
        direction = 'b'
    elif clean.startswith('No-') or clean.startswith('NB-'):
        direction = 'f'  # nollie/nosebutters treated as forward
    else:
        direction = 'b' if 'b-' in t else 'f'
    
    spin = parse_spin(t)
    
    # Grab - last meaningful token
    grab_map = {
        'Wed':'Wed','Weddle':'Wed','Ng':'Ng','Tg':'Tg','Me':'Me','Mel':'Me',
        'I':'I','Nb':'Nb','St':'St','Ro':'Ro','J':'J','Jp':'Jp','Ddr':'Ddr',
        'Tdr':'Tdr','Go':'Go','Bd':'Bd','Mu':'Mu','Ti':'Ti','TK':'TK',
        'Hb':'Hb','Ste':'Ste','RB':'RB','Bb':'Bb','sa':'sa','Ind':'Ind',
        'Slb':'Slb','pb':'pb','oT':'oT','Gor':'Go'
    }
    grab = None
    parts = t.replace('to-','').split('-')
    for p in reversed(parts):
        p2 = p.strip()
        if p2 in grab_map:
            grab = grab_map[p2]
            break
    
    return switch, direction, spin, double_cork, triple_cork, rodeo, grab

# difficulty score 1-10
def difficulty(spin, switch, dbl, trpl, rodeo, trick_raw):
    if spin is None: return None
    base = {180:1,300:2,500:3,700:4,900:5,1080:6,1260:7,1440:8,1600:8,1800:9,1980:10,2160:10}
    d = base.get(spin, 6)
    if switch == 'Y': d += 0.5
    if dbl == 'Y': d += 1.5
    if trpl == 'Y': d += 2
    if rodeo == 'Y': d += 0.5
    return min(round(d, 1), 10)

